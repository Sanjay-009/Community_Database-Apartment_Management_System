    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String id = t1.getText();
        try{
        Class.forName("com.mysql.cj.jdbc.Driver");
        Connection con = (Connection)DriverManager.getConnection("jdbc:mysql://Localhost:3306/community","root","1234");
        Statement stmt = (Statement)con.createStatement();
        String query = "select * from user where resID ='"+id+"'";
        ResultSet rs = stmt.executeQuery(query);
        if(rs.next())
        {
            String name = rs.getString("resName");
            String f = rs.getString("flatNo");
            String block = rs.getString("block");
            String mail = rs.getString("email");
            
            t2.setText(name);
            t3.setText(f);
            t4.setText(block);
            t5.setText(mail); 
        }
        
        else{
        JOptionPane.showMessageDialog(this,"Sorry! The resident ID is invalid ");
        }
        }
        catch(Exception e){
        JOptionPane.showMessageDialog(this,e.getMessage());
        }
    }

